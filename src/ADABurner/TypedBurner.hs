{-# INLINABLE mkValidator #-}
mkValidator :: () -> () -> ScriptContext -> Bool
mkValidator _ _ _ = False
  

data Burner
instance Scripts.ValidatorTypes Burner where
    type instance DatumType Burner = ()
    type instance RedeemerType Burner = ()

typedValidator :: Scripts.TypedValidator Burner
typedValidator = Scripts.mkTypedValidator @Burner
    $$(PlutusTx.compile [|| mkValidator ||])
    $$(PlutusTx.compile [|| wrap ||])
  where
    wrap = Scripts.wrapValidator @() @()

validator :: Validator
validator = Scripts.validatorScript typedValidator

valHash :: Ledger.ValidatorHash
valHash = Scripts.validatorHash typedValidator

scrAddress :: Ledger.Address
scrAddress = scriptAddress validator
